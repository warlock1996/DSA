let count = 0
let innercount = 0
function insertionSort() {
	for (let i = 1; i <= A.length; i++) {
		let ptr = i
		count++
		while (A[ptr] < A[ptr - 1]) {
			innercount++
			const temp = A[ptr - 1]
			A[ptr - 1] = A[ptr]
			A[ptr] = temp
			if (ptr > 1) {
				ptr--
			}
		}
	}
	console.log(A, count, innercount)
}

